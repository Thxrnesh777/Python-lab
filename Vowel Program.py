A1 = input("Enter The Character:")

if(A1=="a" or A1=="e" or A1=="i" or A1=="o" or A1=="u"):
         print("It is Vowel")

elif(A1=="+" or A1=="-" or A1=="*" or A1=="/"):
         print("It is Operator")

elif(A1=="_" or A1=="#"):
         print("It is Special Character")

else:
    print("It is Consonant")
