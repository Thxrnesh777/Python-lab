Mark = int(input("Enter the Mark:"))

if(Mark >=90 and Mark <=100):
    print(" S Grade")

elif(Mark >=80 and Mark <=89):
    print(" A Grade")

elif(Mark >=70 and Mark <=79):
    print(" B Grade")

elif(Mark >=60 and Mark <=69):
    print(" C Grade")

elif(Mark >=50 and Mark <=59):
    print(" D Grade")

elif(Mark >=40 and Mark <=49):
    print(" E Grade")

else:
    print(" F Grade")



